    public void Cambio() {
        if (banderacambio == 0) {
            txtcodigo.setBackground(Color.red);
            txtprecio.setBackground(Color.red);
            txtcantidad.setBackground(Color.red);
            txtdescripcion.setBackground(Color.red);
            txtcantidad.setText("-1");
            txtcantidad.setEditable(false);
            if (!"".equals(txtprecio.getText())) {
                String temporal = txtprecio.getText();
                String temporalcorto = temporal.substring(1, temporal.length());
                double temporaldouble = Double.parseDouble(temporalcorto); // ALGORITMO PARA EL VALOR NEGATIVO. 
                double negativo = temporaldouble * 2;
                double negativofinal = temporaldouble - negativo;
                Facturacion.txtprecio.setText("â‚¬" + Double.toString(negativofinal));
            }
            banderacambio = 1;
        } else if (banderacambio == 1) {

            txtcodigo.setBackground(Color.white);
            txtprecio.setBackground(Color.white);
            txtcantidad.setBackground(Color.white);
            txtdescripcion.setBackground(Color.white);
            txtcantidad.setText("1");
            txtcantidad.setEditable(true);
            if (!"".equals(txtprecio.getText())) {
                String temporal = txtprecio.getText();
                String temporalcorto = temporal.substring(1, temporal.length());
                double temporaldouble = Double.parseDouble(temporalcorto); // ALGORITMO PARA EL VALOR NEGATIVO. 
                double negativo = temporaldouble * 2;
                double negativofinal = temporaldouble - negativo;
                Facturacion.txtprecio.setText("â‚¬" + Double.toString(negativofinal));
            }
            banderacambio = 0;
        }

    }

