    private void txtcodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtcodigoFocusLost
        if (!"".equals(txtcodigo.getText())) {    // VALIDO QUE EL CAMPO NO ESTE VACIO PARA QUE NO ME MUESTRE SIEMPRE EL MENSAJE DE "NO HAY STOCK".

            if (banderacambio == 0) {  // SI EL FOCUS SE PIERDE, VALIDA SI EL PRODUCTO TIPEADO ESTA EN SISTEMA. 
                Principal.bd.Stock();
                Principal.lista.Buscar();
                if (Lista.bandera == 0) {
                    aiuda = 1;
                    txtcantidad.setText("");
                    txtprecio.setText("");
                    txtdescripcion.setText("");
                }

                Lista.bandera = 0;
            } else if (banderacambio == 1) {
                Principal.bd.StockEmpresa();
                if (BaseDatos.banderaempresa == 0) {

                    JOptionPane.showMessageDialog(null, "El articulo " + txtcodigo.getText() + " no esta registrado en la base de datos.");
                    txtcodigo.setText("");
                    txtcantidad.setText("");
                    txtprecio.setText("");
                    txtdescripcion.setText("");
                }

            }

        }
        BaseDatos.banderaempresa = 0;
    }//GEN-LAST:event_txtcodigoFocusLost

    private void ValidarStock() { // VALIDO SI EL STOCK REQUERIDO ES MAYOR AL DISPONIBLE EN SISTEMA.

        boolean bandera;

        for (int i = 0; i < modelofacturacion.getRowCount(); i++) {

            bandera = Principal.lista.ChequeoStock(String.valueOf(Facturacion.modelofacturacion.getValueAt(i, 0)), String.valueOf(Facturacion.modelofacturacion.getValueAt(i, 3)));

            if (bandera) {
                auxstock = 1;
            }
        }

    }


    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (modelofacturacion.getRowCount() == 0) {

            JOptionPane.showMessageDialog(null, "La lista estÃ¡ vacÃ­a, agregue algÃºn producto para facturar.");
        } else {

            String cadena = labelmonto.getText();
            String cadenacorta = cadena.substring(1, cadena.length());
            double cadenadouble = Double.parseDouble(cadenacorta);

            auxiliarmonto = cadenadouble;
            double montopositivo = auxiliarmonto * -1;
            ValidarStock();

            if (botonvendedor.getText().equals("Vendedor") || botoncajero.getText().equals("Cajero")) {

                JOptionPane.showMessageDialog(null, "Debe seleccionar un cajero y/o vendedor.");

            } else {
                if (auxiliarmonto <= -1 && banderadevolucion == 1) {
                    JOptionPane.showMessageDialog(null, "Debe entregarle en efectivo al cliente: â‚¬ " + montopositivo);
                    MediodePago.imprimiendofactura.Imprimir(cadena);
                } else if (auxiliarmonto <= -1) {
                    JOptionPane.showMessageDialog(null, "El importe total es negativo. Debe ajustar la factura antes de proceder al pago.");

                } else if (auxiliarmonto >= 1) {
                    if (auxstock != 1) {

                        MediodePago.labelimporte.setText("â‚¬" + (double) Math.round(auxiliarmonto * 100d) / 100);
                        MediodePago.labelrestante.setText("â‚¬" + (double) Math.round(auxiliarmonto * 100d) / 100);
                        mediodepago.setVisible(true);
                    }
                } else if (auxiliarmonto > -1 && modelofacturacion.getRowCount() != 0) {
                    if (auxstock != 1) {
                        interfazcambio.setVisible(true);
                    }
                }

            }
        }
        auxstock = 0; // REINICIO LA BANDERA.
    }//GEN-LAST:event_jButton6ActionPerformed

