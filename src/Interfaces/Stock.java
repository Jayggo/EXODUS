    private void txtfiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfiltroKeyTyped

        // PARA FILTRAR LA BUSQUEDA
        txtfiltro.addKeyListener(new KeyAdapter() { // PARA FILTRAR LA BUSQUEDA INGRESANDO POR TECLADO. 
            @Override
            public void keyReleased(KeyEvent e) {

                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtfiltro.getText(), 0)); // EL ?i LO QUE HACE ES NO DISTINGUIR ENTRE MAYUSCULAS Y MINUSCULAS. 

            }

        });

        trs = new TableRowSorter(modelo);
        tabla.setRowSorter(trs);


    }//GEN-LAST:event_txtfiltroKeyTyped

    private void botonaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonaceptarActionPerformed

        try {
            int seleccion = tabla.getSelectedRow();
            String cod = String.valueOf(modelo.getValueAt(seleccion, 0));
            String desc = String.valueOf(modelo.getValueAt(seleccion, 1));
            String pre = String.valueOf(modelo.getValueAt(seleccion, 2));
            double montototal = 0.0;
            int itemstotal = 0;

            if (Egresos.banderaegresos) {

                Principal.lista.ListaEgresos(cod, desc, pre);

                for (int i = 0; i < Egresos.modeloegresos.getRowCount(); i++) {
                    String cadenaprecio = String.valueOf(Egresos.tablaegresos.getValueAt(i, 2));
                    String cadenacorta = cadenaprecio.substring(1, cadenaprecio.length());   // ALGORITMO PARA SUMAR LOS PRECIOS DE LA TABLA AL TOTAL A ABONAR.
                    double preciofinal = Double.parseDouble(cadenacorta);
                    montototal += preciofinal;

                    String cadenaitem = String.valueOf(Egresos.tablaegresos.getValueAt(i, 3)); //ALGORITMO PARA SUMAR LOS ITEMS. 
                    int itementero = Integer.parseInt(cadenaitem);
                    itemstotal += itementero;
                }
                Egresos.labeltotal.setText("â‚¬ " + (double) Math.round(montototal * 100d) / 100);
                Egresos.labelarticulos.setText(Integer.toString(itemstotal));
                this.setVisible(false);
                modelo.setRowCount(0);
                Egresos.banderaegresos = false;
            } else {
                Principal.lista.MostrarLista();
                this.setVisible(false);
                modelo.setRowCount(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Seleccione un articulo");
        }
    }//GEN-LAST:event_botonaceptarActionPerformed

